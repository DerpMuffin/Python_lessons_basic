    result = []
    temp = ''
    temp_lower = ''
    for c in string:
        if c.islower():
            temp_lower += c
            if len(temp) > 2:
                result.append(temp[:-2])
            temp = ''
        else:
            if len(temp_lower) >= 2:
                temp += c
            elif len(temp) != 0:
                temp += c
            temp_lower = ''
    if len(temp) > 2:
        result.append(temp[:-2])
    return result


