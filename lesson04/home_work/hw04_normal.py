def fill_file(filename):
    content = ''
    while len(content) < 2500:
        content += str(random.randint(0, 9))
    with open(filename, 'w') as file:
        file.write(content)


def get_max_line_same_nums(filename):

    with open(filename, 'r') as file:
        content = file.readline()
    max_line = ''
    temp = ''
    for c in content:
        if c not in temp:
            if len(max_line) < len(temp):
                max_line = temp
            temp = ''
        temp += c
    return max_line


