def parse_lower_chars(string):
    result = []
    temp = ''
    upper_find = False
    for c in string:
        if c.islower():
            temp += c
        else:
            upper_find = True
            if len(temp) != 0:
                result.append(temp)
                temp = ''
    if upper_find and temp:
        result.append(temp)
    return result


